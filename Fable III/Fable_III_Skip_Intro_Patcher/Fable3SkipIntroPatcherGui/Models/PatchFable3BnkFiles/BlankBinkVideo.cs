	public static void ReplaceAllIntroVideos(ref BnkContentFileContents ContentFileContents, ref BnkDecompressedIndexData DecompressedIndexData, bool IsContentDataCompressed)
	{
		for (int i = 0; i < DecompressedIndexData.NumberOfFiles; i++)
		{
			if (DecompressedIndexData.AllFileEntries[i].FilePath.Equals("art\\videos\\lionhead_logo.bik") || DecompressedIndexData.AllFileEntries[i].FilePath.Equals("art\\videos\\microsoft_logo.bik"))
			{
				// i should be 35 for the Microsoft video and 38 for Lionhead
				replaceBnkContentFileEntry(i, ref ContentFileContents, ref DecompressedIndexData, IsContentDataCompressed);
			}
		}
	}

	private static void replaceBnkContentFileEntry(int I, ref BnkContentFileContents ContentFileContents, ref BnkDecompressedIndexData DecompressedIndexData, bool IsContentDataCompressed)
	{
		if (!IsContentDataCompressed)
		{
			ContentFileContents.FileData[I].ContentFileData = emptyBinkVideo;
			ContentFileContents.FileData[I].Padding = emptyBinkVideoPadding;

			DecompressedIndexData.AllFileEntries[I].ContentFileDataSize = emptyBinkVideo.Length;

			for (int j = I; j < DecompressedIndexData.NumberOfFiles; j++)
			{
				BnkContentFileEntry previousIndexEntry = DecompressedIndexData.AllFileEntries[j - 1];
				BnkContentFileData previousFileEntry = ContentFileContents.FileData[j - 1];
				uint currentFileActualOffset = (uint)(previousIndexEntry.FileOffset + previousFileEntry.ContentFileData.Length + previousFileEntry.Padding.Length);
				DecompressedIndexData.AllFileEntries[j].FileOffset = currentFileActualOffset;
			}

			return;
		}

		throw new NotImplementedException($"{nameof(BlankBinkVideo)}: Video replacement in compressed BNK files has not been implemented.");
	}
